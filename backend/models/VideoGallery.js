import mongoose from "mongoose";

const videoGallerySchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    videoUrl: {
      type: String,
      required: true,
      trim: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const VideoGallery = mongoose.model("VideoGallery", videoGallerySchema);
export default VideoGallery;
